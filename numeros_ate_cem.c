#include <stdio.h>
#include <stdlib.h>
#include <locale.h>

// Loop for 

int main(){
    for (int i = 0; i <= 100; i++){
        printf("Imprimindo o numero %d de 100\n", i);
    }
}

// Loop while 
int main(){
    int i = 0;
    while (i <= 100){
        printf("Imprimindo o numero %d de 100\n", i);
        i++;
    }
}

